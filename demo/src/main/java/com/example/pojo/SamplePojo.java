package com.example.pojo;

public class SamplePojo {

	String employer;
	String technology;
	String published_date;
	String executed_by;
	/**
	 * @return the employer
	 */
	public String getEmployer() {
		return employer;
	}
	/**
	 * @param employer the employer to set
	 */
	public void setEmployer(String employer) {
		this.employer = employer;
	}
	/**
	 * @return the technology
	 */
	public String getTechnology() {
		return technology;
	}
	/**
	 * @param technology the technology to set
	 */
	public void setTechnology(String technology) {
		this.technology = technology;
	}
	/**
	 * @return the published_date
	 */
	public String getPublished_date() {
		return published_date;
	}
	/**
	 * @param published_date the published_date to set
	 */
	public void setPublished_date(String published_date) {
		this.published_date = published_date;
	}
	/**
	 * @return the executed_by
	 */
	public String getExecuted_by() {
		return executed_by;
	}
	/**
	 * @param executed_by the executed_by to set
	 */
	public void setExecuted_by(String executed_by) {
		this.executed_by = executed_by;
	}
	
	
	
}
